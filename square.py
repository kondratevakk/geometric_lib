def area(a):
    """
    Вычисляет площадь квадрата по заданной длине его стороны.

    Параметры:
    a (float): Длина стороны квадрата.

    Возвращает:
    float: Площадь квадрата.

    Пример вызова:
    >>> area(4)
    16
    """
    return a * a


def perimeter(a):
    """
    Вычисляет периметр квадрата по заданной длине его стороны.

    Параметры:
    a (float): Длина стороны квадрата.

    Возвращает:
    float: Периметр квадрата.

    Пример вызова:
    >>> perimeter(4)
    16
    """
    return 4 * a
